export function createOrderFulfillmentConfig(
  orders: any[],
  dateRange?: { from: string; to: string }
): ExportConfig {
  const formatCurrency = (amount: number) => `PHP ${amount.toLocaleString()}`;
  const formatDateStr = (dateString: string) =>
    new Date(dateString).toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
      year: 'numeric',
      timeZone: 'Asia/Manila',
    });

  const statusCounts = orders.reduce((acc: Record<string, number>, order) => {
    const status = order.status || 'Unknown';
    acc[status] = (acc[status] || 0) + 1;
    return acc;
  }, {});

  return {
    title: 'Order Fulfillment Report',
    subtitle: 'Orders by fulfillment status',
    filename: 'order_fulfillment_report',
    headers: [
      'Order Number',
      'Customer',
      'Amount',
      'Status',
      'Payment Status',
      'Date',
    ],
    rows: orders.map(order => [
      order.orderNumber,
      order.customerName,
      formatCurrency(order.amount),
      order.status,
      order.paymentStatus || 'N/A',
      formatDateStr(order.createdAt),
    ]),
    dateRange,
    additionalInfo: Object.entries(statusCounts).map(([status, count]) => ({
      label: `${status} Orders`,
      value: count.toString(),
    })),
  };
}

