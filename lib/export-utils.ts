// lib/export-utils.ts
// Professional export utility with headers, footers, and PDF support

import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';

export interface ExportConfig {
  title: string;
  subtitle?: string;
  filename: string;
  headers: string[];
  rows: (string | number)[][];
  dateRange?: { from: string; to: string };
  additionalInfo?: { label: string; value: string }[];
  companyName?: string;
}

const DEFAULT_COMPANY_NAME = 'SCENTOPIA';

const formatDate = (date: Date): string => {
  return date.toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    timeZone: 'Asia/Manila',
  });
};

const formatDateTime = (date: Date): string => {
  return date.toLocaleString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
    timeZone: 'Asia/Manila',
  });
};

// ============================================
// CSV EXPORT WITH PROFESSIONAL FORMATTING
// ============================================
export function exportToCSV(config: ExportConfig): void {
  const {
    title,
    subtitle,
    filename,
    headers,
    rows,
    dateRange,
    additionalInfo,
    companyName = DEFAULT_COMPANY_NAME,
  } = config;

  const now = new Date();
  const csvRows: string[] = [];
  const colCount = headers.length;

  // Helper to create separator row
  const createSeparator = (char: string = '-') => {
    return Array(colCount).fill(`"${char.repeat(20)}"`).join(',');
  };

  // Helper to create empty row spanning all columns
  const emptyRow = () => Array(colCount).fill('""').join(',');

  // Helper to create a centered text row (text in first cell, rest empty)
  const centeredRow = (text: string) => {
    const cells = [`"${text}"`];
    for (let i = 1; i < colCount; i++) cells.push('""');
    return cells.join(',');
  };

  // Helper to create a label-value row
  const labelValueRow = (label: string, value: string) => {
    const cells = [`"${label}"`, `"${value}"`];
    for (let i = 2; i < colCount; i++) cells.push('""');
    return cells.join(',');
  };

  // ═══════════════════════════════════════════
  // REPORT HEADER
  // ═══════════════════════════════════════════
  csvRows.push(createSeparator('═'));
  csvRows.push(centeredRow(companyName));
  csvRows.push(centeredRow(title.toUpperCase()));
  if (subtitle) {
    csvRows.push(centeredRow(subtitle));
  }
  csvRows.push(createSeparator('═'));
  csvRows.push(emptyRow());

  // ═══════════════════════════════════════════
  // REPORT INFORMATION
  // ═══════════════════════════════════════════
  csvRows.push(centeredRow('REPORT INFORMATION'));
  csvRows.push(createSeparator('-'));
  csvRows.push(labelValueRow('Report Generated', formatDateTime(now)));
  csvRows.push(labelValueRow('Report Type', title));
  if (dateRange) {
    csvRows.push(labelValueRow('Date Range', `${dateRange.from} to ${dateRange.to}`));
  }
  csvRows.push(labelValueRow('Total Records', rows.length.toLocaleString()));

  // Additional info section
  if (additionalInfo && additionalInfo.length > 0) {
    csvRows.push(emptyRow());
    csvRows.push(centeredRow('SUMMARY'));
    csvRows.push(createSeparator('-'));
    additionalInfo.forEach(info => {
      csvRows.push(labelValueRow(info.label, info.value));
    });
  }

  csvRows.push(createSeparator('-'));
  csvRows.push(emptyRow());

  // ═══════════════════════════════════════════
  // DATA TABLE
  // ═══════════════════════════════════════════
  csvRows.push(centeredRow('DATA'));
  csvRows.push(createSeparator('═'));

  // Column headers (bold effect via caps)
  csvRows.push(headers.map(h => `"${h.toUpperCase()}"`).join(','));
  csvRows.push(createSeparator('-'));

  // Data rows
  rows.forEach((row, index) => {
    csvRows.push(row.map(cell => {
      const cellStr = String(cell);
      // Escape quotes and wrap in quotes
      return `"${cellStr.replace(/"/g, '""')}"`;
    }).join(','));
  });

  // ═══════════════════════════════════════════
  // REPORT FOOTER
  // ═══════════════════════════════════════════
  csvRows.push(createSeparator('═'));
  csvRows.push(emptyRow());
  csvRows.push(centeredRow(`Total Records: ${rows.length.toLocaleString()}`));
  csvRows.push(emptyRow());
  csvRows.push(createSeparator('-'));
  csvRows.push(centeredRow('END OF REPORT'));
  csvRows.push(createSeparator('-'));
  csvRows.push(emptyRow());
  csvRows.push(labelValueRow('Company', companyName));
  csvRows.push(labelValueRow('Classification', 'Confidential Business Report'));
  csvRows.push(labelValueRow('Report ID', `RPT-${now.getTime()}`));
  csvRows.push(labelValueRow('Generated By', 'System Auto-Generated'));
  csvRows.push(emptyRow());
  csvRows.push(centeredRow('For questions or concerns, please contact the system administrator.'));

  // Create and download the file
  const csvContent = csvRows.join('\n');
  const blob = new Blob(['\ufeff' + csvContent], { type: 'text/csv;charset=utf-8;' }); // BOM for Excel
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = `${filename}_${now.toISOString().split('T')[0]}.csv`;
  link.click();
  URL.revokeObjectURL(url);
}

// ============================================
// PDF EXPORT WITH HEADERS/FOOTERS
// ============================================
export function exportToPDF(config: ExportConfig): void {
  const {
    title,
    subtitle,
    filename,
    headers,
    rows,
    dateRange,
    additionalInfo,
    companyName = DEFAULT_COMPANY_NAME,
  } = config;

  const now = new Date();
  const doc = new jsPDF({
    orientation: rows[0]?.length > 6 ? 'landscape' : 'portrait',
    unit: 'mm',
    format: 'a4',
  });

  const pageWidth = doc.internal.pageSize.getWidth();
  const pageHeight = doc.internal.pageSize.getHeight();
  const margin = 15;

  // === HEADER ===
  // Company name
  doc.setFontSize(20);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(212, 175, 55); // Gold color
  doc.text(companyName, pageWidth / 2, 20, { align: 'center' });

  // Title
  doc.setFontSize(16);
  doc.setTextColor(0, 0, 0);
  doc.text(title, pageWidth / 2, 30, { align: 'center' });

  // Subtitle
  if (subtitle) {
    doc.setFontSize(11);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text(subtitle, pageWidth / 2, 37, { align: 'center' });
  }

  // Divider line
  doc.setDrawColor(212, 175, 55);
  doc.setLineWidth(0.5);
  doc.line(margin, 42, pageWidth - margin, 42);

  // Report info
  let infoY = 48;
  doc.setFontSize(9);
  doc.setTextColor(80, 80, 80);
  doc.setFont('helvetica', 'normal');

  doc.text(`Generated: ${formatDateTime(now)}`, margin, infoY);
  infoY += 5;

  if (dateRange) {
    doc.text(`Date Range: ${dateRange.from} to ${dateRange.to}`, margin, infoY);
    infoY += 5;
  }

  if (additionalInfo) {
    additionalInfo.forEach(info => {
      doc.text(`${info.label}: ${info.value}`, margin, infoY);
      infoY += 5;
    });
  }

  doc.text(`Total Records: ${rows.length}`, margin, infoY);
  infoY += 8;

  // === DATA TABLE ===
  autoTable(doc, {
    head: [headers],
    body: rows.map(row => row.map(cell => String(cell))),
    startY: infoY,
    margin: { left: margin, right: margin },
    styles: {
      fontSize: 8,
      cellPadding: 3,
      textColor: [30, 30, 30],
      lineColor: [200, 200, 200],
      lineWidth: 0.1,
    },
    headStyles: {
      fillColor: [212, 175, 55],
      textColor: [10, 10, 10],
      fontStyle: 'bold',
      fontSize: 9,
    },
    alternateRowStyles: {
      fillColor: [250, 248, 245],
    },
    didDrawPage: (data) => {
      // Footer on each page
      const pageCount = doc.getNumberOfPages();
      const currentPage = data.pageNumber;

      doc.setFontSize(8);
      doc.setTextColor(120, 120, 120);

      // Left footer
      doc.text(
        `${companyName} - Confidential Business Report`,
        margin,
        pageHeight - 10
      );

      // Right footer - page number
      doc.text(
        `Page ${currentPage} of ${pageCount}`,
        pageWidth - margin,
        pageHeight - 10,
        { align: 'right' }
      );

      // Center footer - report ID
      doc.text(
        `Report ID: RPT-${now.getTime()}`,
        pageWidth / 2,
        pageHeight - 10,
        { align: 'center' }
      );
    },
  });

  // Final page footer enhancement
  const finalY = (doc as any).lastAutoTable?.finalY || infoY + 50;

  if (finalY < pageHeight - 40) {
    doc.setDrawColor(212, 175, 55);
    doc.setLineWidth(0.3);
    doc.line(margin, finalY + 10, pageWidth - margin, finalY + 10);

    doc.setFontSize(8);
    doc.setTextColor(100, 100, 100);
    doc.text('--- End of Report ---', pageWidth / 2, finalY + 17, { align: 'center' });
    doc.text(
      'This report was automatically generated. For questions, contact the administrator.',
      pageWidth / 2,
      finalY + 22,
      { align: 'center' }
    );
  }

  // Save the PDF
  doc.save(`${filename}_${now.toISOString().split('T')[0]}.pdf`);
}

// ============================================
// UNIFIED EXPORT FUNCTION
// ============================================
export type ExportFormat = 'csv' | 'pdf';

export function exportReport(config: ExportConfig, format: ExportFormat): void {
  if (format === 'pdf') {
    exportToPDF(config);
  } else {
    exportToCSV(config);
  }
}

