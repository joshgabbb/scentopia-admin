export function createOrdersExportConfig(
  orders: any[],
  dateRange?: { from: string; to: string }
): ExportConfig {
  const formatCurrency = (amount: number) => `PHP ${amount.toLocaleString()}`;
  const formatDateStr = (dateString: string) =>
    new Date(dateString).toLocaleDateString('en-US', {
      month: 'short',
      day: 'numeric',
      year: 'numeric',
      timeZone: 'Asia/Manila',
    });

  return {
    title: 'Orders Report',
    subtitle: 'Complete order transaction details',
    filename: 'orders_report',
    headers: [
      'Order Number',
      'Customer Name',
      'Email',
      'Amount',
      'Order Status',
      'Payment Status',
      'Payment Method',
      'Items',
      'Date',
    ],
    rows: orders.map(order => [
      order.orderNumber,
      order.customerName,
      order.customerEmail,
      formatCurrency(order.amount),
      order.status,
      order.payment?.paymentStatus || 'N/A',
      order.payment?.paymentMethod || 'N/A',
      order.itemCount,
      formatDateStr(order.createdAt),
    ]),
    dateRange,
    additionalInfo: [
      { label: 'Total Orders', value: orders.length.toString() },
      { label: 'Total Revenue', value: formatCurrency(orders.reduce((sum, o) => sum + o.amount, 0)) },
    ],
  };
}

