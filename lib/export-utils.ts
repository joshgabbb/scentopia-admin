// SALES REPORT CONFIGS
// ============================================
export function createSalesReportConfig(
  sales: any[],
  reportType: 'daily' | 'weekly' | 'monthly',
  dateRange?: { from: string; to: string }
): ExportConfig {
  const formatCurrency = (amount: number) => `PHP ${amount.toLocaleString()}`;

  const titles: Record<string, string> = {
    'daily': 'Daily Sales Report',
    'weekly': 'Weekly Sales Report',
    'monthly': 'Monthly Sales Report',
  };

  const subtitles: Record<string, string> = {
    'daily': 'Sales breakdown by day',
    'weekly': 'Sales breakdown by week',
    'monthly': 'Sales breakdown by month',
  };

  const totalRevenue = sales.reduce((sum, s) => sum + (s.revenue || 0), 0);
  const totalOrders = sales.reduce((sum, s) => sum + (s.orderCount || 0), 0);

  return {
    title: titles[reportType],
    subtitle: subtitles[reportType],
    filename: `sales_${reportType}_report`,
    headers: [
      'Period',
      'Orders',
      'Revenue',
      'Average Order Value',
      'Items Sold',
    ],
    rows: sales.map(sale => [
      sale.period,
      sale.orderCount,
      formatCurrency(sale.revenue),
      formatCurrency(sale.averageOrderValue || (sale.revenue / (sale.orderCount || 1))),
      sale.itemsSold || 0,
    ]),
    dateRange,
    additionalInfo: [
      { label: 'Total Revenue', value: formatCurrency(totalRevenue) },
      { label: 'Total Orders', value: totalOrders.toLocaleString() },
      { label: 'Average Order Value', value: formatCurrency(totalRevenue / (totalOrders || 1)) },
    ],
  };
}

